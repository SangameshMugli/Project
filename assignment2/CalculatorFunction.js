// 3. Create a calculator using function for each operartions -  sum, subraction, multiplication and division of 2 integers provided by user


function sum(a,b)
{
   return a+b
}

function sub(a,b)
{
    return a-b
}

function mul(a,b)
{
    return a*b
}

function div(a,b)
{
    return a/b

}
console.log(sum(10,20))
console.log(sub(19,7))
console.log(mul(10,30))
console.log(div(10,5))


// output

// 30-sum
// 12-sub
// 300-mul
// 2-div